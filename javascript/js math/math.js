console.log("Round method: ",Math.round(3.5)); //it's output will be 4 if the fraction part <.5 then it will be 3

console.log("Ceil Method: ",Math.ceil(3.5));//whatever we will put into ceil method it will show next number 

console.log("Floor method: ",Math.floor(3.5));//whatever we will put into floor method it will show below number\

console.log("Power Method",Math.pow(8,2));//this means 8 square

console.log("square method: ",Math.sqrt(64));//square of 64

console.log('absolute:',Math.abs(-3.6));//absolute value will be shown

console.log("Minimum: ",Math.min(10,20,30,40,50,60,-5));//minimum value will be shown

console.log("Maximun",Math.max(10,20,40,60,70,90));//maximun number will be shown 




